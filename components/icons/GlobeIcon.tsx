type Props = {
  timeZone: string | undefined
}

export default function GlobeIcon({ timeZone = '' }: Props) {
  if (timeZone.startsWith('America/')) {
    return <GlobeAmericas />
  }
  if (
    timeZone.startsWith('Africa/') || timeZone.startsWith('Europe/')
  ) {
    return <GlobeEuropeAfrica />
  }
  if (
    timeZone.startsWith('Asia/') || timeZone.startsWith('Australia/')
  ) {
    return <GlobeAsiaAustralia />
  }

  // Sorry Antarctica, Atlantic, Indian & Pacific time zones, no specific icon for you :-(
  return <Globe />
}

